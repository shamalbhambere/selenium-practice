
public class CharAlphabetORNot {

	public static void main(String[] args) {

        char c = 'A';
        
        if((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z')){
        	System.out.println("Character is Alphabet");
        }else {
        	System.out.println("Character is not Alphabet");
        }
		

	}

}
